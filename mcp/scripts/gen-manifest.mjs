// Generates src/generated/calendars.ts by scanning the repo's data/<year>.json
// files. The set of years is derived from the files that actually exist on disk
// (NOT a hardcoded numeric range like the frontend's state.availableYears), so a
// newly added year is picked up automatically on the next build/dev/test run.
//
// Each year's JSON is statically imported so the bundler (esbuild via wrangler)
// inlines the data into the Worker. The Worker therefore reads calendar data
// from its own bundle — it never fetches jsDelivr or any external CDN at runtime.
import { readdirSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const dataDir = join(here, "..", "..", "data");
const outDir = join(here, "..", "src", "generated");
const outFile = join(outDir, "calendars.ts");

const years = readdirSync(dataDir)
  .map((name) => /^(\d{4})\.json$/.exec(name))
  .filter((m) => m !== null)
  .map((m) => Number(m[1]))
  .sort((a, b) => a - b);

if (years.length === 0) {
  throw new Error(`No data/<year>.json files found in ${dataDir}`);
}

const imports = years
  .map((y) => `import y${y} from "../../../data/${y}.json";`)
  .join("\n");
const entries = years.map((y) => `  ${y}: y${y} as unknown as Calendar,`).join("\n");

const content = `// AUTO-GENERATED by scripts/gen-manifest.mjs — do not edit by hand.
// Years are derived from the data/<year>.json files present on disk.
import type { Calendar } from "../engine/types.js";

${imports}

export const CALENDARS: Record<number, Calendar> = {
${entries}
};
`;

mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, content);
console.log(`Generated ${outFile} with years: ${years.join(", ")}`);
